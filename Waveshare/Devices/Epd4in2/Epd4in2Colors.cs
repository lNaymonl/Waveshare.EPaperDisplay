namespace Waveshare.Devices.Epd4in2 {
    internal class Epd4in2Colors {
        /// <summary>
        /// Byte value for color white
        /// </summary>
        public const byte White = 0xff;
        /// <summary>
        /// Byte value for color light gray
        /// </summary>
        public const byte LighterGray = 0xC0;
        /// <summary>
        /// Byte value for color gray
        /// </summary>
        public const byte Gray = 0x80;
        /// <summary>
        /// Byte value for color black
        /// </summary>
        public const byte Black = 0x00;
    }
}